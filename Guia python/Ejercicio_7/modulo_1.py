# - suma, resta, multiplicación, división

def suma():
    num1 = int(input("Ingrese el primer numero para realizar la operación: "))
    num2 = int(input("Ingrese el segundo numero para realizar la operación: "))
    return num1 + num2

def resta():
    num1 = int(input("Ingrese el primer numero para realizar la operación: "))
    num2 = int(input("Ingrese el segundo numero para realizar la operación: "))
    return num1 - num2

def multiplicación():
    num1 = int(input("Ingrese el primer numero para realizar la operación: "))
    num2 = int(input("Ingrese el segundo numero para realizar la operación: "))
    return num1 * num2

def división():
    num1 = int(input("Ingrese el primer numero para realizar la operación: "))
    num2 = int(input("Ingrese el segundo numero para realizar la operación: "))
    return num1/num2